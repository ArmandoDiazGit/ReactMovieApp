export const getMovieById = (id) => {
  return movies.find(movie => movie.id === parseInt(id));
};

export const getMoviesByCategory = (category) => {
  if (category === 'all') return movies;
  return movies.filter(movie => movie.category === category);
};

export const searchMovies = (query) => {
  const lowercaseQuery = query.toLowerCase();
  return movies.filter(movie => 
    movie.title.toLowerCase().includes(lowercaseQuery) ||
    movie.original_title.toLowerCase().includes(lowercaseQuery) ||
    movie.overview.toLowerCase().includes(lowercaseQuery) ||
    movie.genres.some(genre => genre.toLowerCase().includes(lowercaseQuery))
  );
};

export const getFeaturedMovies = () => {
  return movies.filter(movie => movie.vote_average >= 8.0).slice(0, 5);
};

export const getTrendingMovies = () => {
  return [...movies].sort((a, b) => b.popularity - a.popularity).slice(0, 10);
};
